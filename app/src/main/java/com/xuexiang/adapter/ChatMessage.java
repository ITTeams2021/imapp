package com.xuexiang.adapter;

import android.text.SpannableString;

public class ChatMessage {
    private String name;
    private String msg;

    public ChatMessage() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

}